public class Loop {
    public static void main(String[] args) {
        for (int j = 1; j <= 10; j++) {
            System.out.println("Iteration For Loop: " + j);
        }


        int i = 1; 
        while (i <= 10) {
            System.out.println("Iteration While Loop: " + i);
            i++;
        }
    }
}